    private void signInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInActionPerformed
        username = userNameInput.getText();
        passWord = passwordInput.getText();
        if(username.isEmpty()||passWord.isEmpty()){
            success.setText("Login failed, please enter username and password.");
        }
        else{
            try{
            Connection conn = ComputerScienceIA.connection();
            Statement statement = conn.createStatement();
            ResultSet result = statement.executeQuery("SELECT * from userProfile WHERE userName = '"+username+"'");
            if (result.next()) {
                if (result.getString("password").equals(passWord)) {
                    success.setText("Login successful!");
                    setType(username);
                    HomePage userHome = new HomePage();
                    userHome.setVisible(true);
                    userHome.setLocationRelativeTo(this);
                    this.dispose();
                } else {
                    success.setText("Login failed, please check your password.");
                }
            } else {
                success.setText("Login failed, username not found.");
            }
        } catch (Exception e){
            e.printStackTrace();
            System.out.println("Error occured");
        }
        }
    }//GEN-LAST:event_signInActionPerformed

