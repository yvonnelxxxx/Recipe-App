    private void signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpActionPerformed
        username = userNameInput.getText();
        passWord = passwordInput.getText();
        if (username.isEmpty() || passWord.isEmpty()) {
            success.setText("Sign up failed, please enter username and password.");
        } else {
            try {
                Connection conn = ComputerScienceIA.connection();
                String insertQuery = "INSERT INTO `userProfile`(`userName`,`password`,`userType`) VALUES (?, ?, 'Normal')";
                PreparedStatement insertStatement = conn.prepareStatement(insertQuery);
                insertStatement.setString(1, username);
                insertStatement.setString(2, passWord);
                insertStatement.executeUpdate();
                success.setText("Sign up success, Logging in...");
                HomePage userHome = new HomePage();
                userHome.setVisible(true);
                userHome.setLocationRelativeTo(this);
                this.dispose();
            } catch (Exception e) {
                e.printStackTrace();
                System.out.println("Error occurred");
            }
        }
    }//GEN-LAST:event_signUpActionPerformed

